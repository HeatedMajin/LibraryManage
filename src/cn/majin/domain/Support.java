package cn.majin.domain;

//保存一条 类似的书本名称 和其相应的数量
public class Support {
	private String name;
	private int count;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

}
